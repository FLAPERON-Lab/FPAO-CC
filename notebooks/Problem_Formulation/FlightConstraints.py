@app.cell
def _():
    mo.md(
        r"""
    ### Assumptions

    Within a FPAO problem, it is sufficient to describe the _position_, _translational velocity_, and _translational acceleration_ of the aircraft with respect to the air or ground.  
    It is therefore typical to regard the aircraft as a point-mass, with no actual geometry,  inertia, _orientation_, _angular velocity_ and _angular acceleration_ with respect to the air or the ground.

    This is done by modelling all external (aerodynamic, propulsive, inertial) forces as acting in one single point, the aircraft Center of gravity (CG), which becomes then representative of the aircraft motion.  

    A point-mass can have at most 3 Degrees of Freedom (DoFs), which correspond to the three translations it can have with respect to the air or the ground.  


    Moreover, regarding the external forces acting on the aircraft, it is going to be assumed that:

    1. The thrust force $\overrightarrow{T}$ is always aligned with the airspeed vector $\overrightarrow{V}$

    2. The pilot is able to fly so that the only aerodynamic force perpendicular to the airspeed vector $\overrightarrow{V}$ is lift.

    The latter assumption implies that turning flight is always executed in a _coordinated_ way, meaning that there is no sideslip angle between the aircraft body and the airspeed vector, hence the lift $\overrightarrow{L}$ and drag $\overrightarrow{D}$ forces act in the symmetry plane of the aircraft, and therefore there is no additional aerodynamic side force.
    """
    )
    return


